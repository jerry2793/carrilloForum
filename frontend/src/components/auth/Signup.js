const useStyles = makeStyles((theme) => ({
  paper: {
    marginTop: theme.spacing(8),
    display: 'flex',
    flexDirection: 'column',
    alignItems: 'center',
  },
  avatar: {
    margin: theme.spacing(1),
    backgroundColor: theme.palette.secondary.main,
  },
  form: {
    width: '100%', // Fix IE 11 issue.
    marginTop: theme.spacing(3),
  },
  submit: {
    margin: theme.spacing(3, 0, 2),
  },
}));


const renderInput = ({ input, meta, ...props }) => {
  return (<TextField 
      inputProps={{
        autoComplete: 'off'
      }}
      error={meta.touched && meta.error}
      helperText={meta.touched? meta.error : ''}
      {...input}
      {...props}
    />)
}

const required = (value, allValues, props, name) => {
  if (!value) {
    // console.log('invalid ', name)
    return `Required input`
  } else {
    return undefined
  }
}

function Signup(props) {
  const classes = useStyles();

  const {
    handleSubmit,
    invalid, 
    submitting,
    signup,
    history,
  } = props
  
  const onSubmit = formProps => {
    signup(formProps, () => {
      history.push('/feature');
    });
  };

  return (
    <Container component="main" maxWidth="xs">
      <CssBaseline />
      <div className={classes.paper}>
        <Avatar className={classes.avatar}>
          <LockOutlinedIcon />
        </Avatar>
        <Typography component="h1" variant="h5">
          Sign up
        </Typography>
        <form className={classes.form} noValidate>
          <Grid container spacing={2}>
            <Grid item xs={12} sm={6}>
              <Field
                validate={required}
                component={renderInput}
                autoComplete="fname"
                name="firstName"
                variant="outlined"
                required
                fullWidth
                id="firstName"
                label="First Name"
                autoFocus
              />
            </Grid>
            <Grid item xs={12} sm={6}>
              <Field
                validate={required}
                component={renderInput}
                variant="outlined"
                required
                fullWidth
                id="lastName"
                label="Last Name"
                name="lastName"
                autoComplete="lname"
              />
            </Grid>
            <Grid item xs={12}>
              <Field
                validate={required}
                component={renderInput}
                variant="outlined"
                required
                fullWidth
                id="email"
                label="Email Address"
                name="email"
                autoComplete="email"
              />
            </Grid>
            <Grid item xs={12}>
              <Field
                validate={required}
                component={renderInput}
                variant="outlined"
                required
                fullWidth
                name="password"
                label="Password"
                type="password"
                id="password"
                autoComplete="current-password"
              />
            </Grid>
            <Grid item xs={12}>
              <Field
                validate={required}
                component={renderInput}
                variant="outlined"
                required
                fullWidth
                name="password2"
                label="Password Again"
                type="password"
                id="password"
                autoComplete="current-password2"
              />
            </Grid>
            <Grid item xs={12}>
              <FormControlLabel
                control={<Checkbox value="allowExtraEmails" color="primary" />}
                label="I want to receive notifications (not setup yet, but in the future yes) via email."
              />
            </Grid>
          </Grid>
          <Button
            type="submit"
            fullWidth
            variant="contained"
            color="primary"
            className={classes.submit}
            onClick={handleSubmit(onSubmit)}
            disabled={submitting || invalid}
          >
            Sign Up
          </Button>
          <small style={{ color: 'red', textAlign: 'center' }}>{props.errorMessage}</small>

          <Grid container justify="flex-end">
            <Grid item>
              <Link href="/signin" variant="body2">
                Already have an account? Sign in
              </Link>
            </Grid>
          </Grid>
        </form>
      </div>
      {/* <Box mt={5}>
        <Copyright />
      </Box> */}
    </Container>
  );
}

const validate = values => {
  const error = {}
  if (values.password !== values.password2) {
    error.password2 = 'Passwords Do not match'
  }
  return error
}

function mapStateToProps(state) {
  return { errorMessage: state.auth.errorMessage };
}

export default compose(
  connect(mapStateToProps, actions),
  reduxForm({ 
    form: 'signup',
    validate
  })
)(Signup);